#include "includes/libft.h"

int					ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t			i;

	if (!n)
		return (1);
	if (!s1 || !s2)
		return (0);
	i = -1;
	if (*s1 != *s2)
		return (0);
	while (*s1 == *s2 && ++i < n && (*s1 != '\0' && *s2 != '\0'))
	{
		s1++;
		s2++;
		if (*s1 != *s2 && (i + 1) < n)
			return (0);
	}
	return (1);
}
